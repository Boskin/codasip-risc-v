#ifndef CODASIPSUBTARGET_H
#define CODASIPSUBTARGET_H

#include "llvm/Target/TargetSubtargetInfo.h"
#include "llvm/Target/TargetMachine.h"

#include "CodasipInstrInfo.h"
#include "CodasipISelLowering.h"
#include "CodasipSelectionDAGInfo.h"
#include "CodasipFrameLowering.h"

#include <string>

#define GET_SUBTARGETINFO_ENUM
#define GET_SUBTARGETINFO_HEADER
#include "CodasipGenSubtargetInfo.inc"

namespace llvm {

class CodasipTargetMachine;

class CodasipSubtarget: public CodasipGenSubtargetInfo
{
  CodasipInstrInfo InstrInfo;
  CodasipFrameLowering FrameInfo;
  CodasipTargetLowering TLInfo;
  CodasipSelectionDAGInfo TSInfo;
  InstrItineraryData InstrItins;
public:
  /// This constructor initializes the data members to match that
  /// of the specified module.
  CodasipSubtarget(const Triple &TT, const std::string &FS, CodasipTargetMachine &TM);
  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options. Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);
  /// Enable this, if you want.
  bool enablePostRAScheduler() const override;
  CodeGenOpt::Level getOptLevelToEnablePostRAScheduler() const override {return CodeGenOpt::None;}
  // Access methods (moved here from TargetMachine in 3.6 (or 3.5)
  const TargetInstrInfo* getInstrInfo() const override {return &InstrInfo;}
  const TargetFrameLowering* getFrameLowering() const override {return &FrameInfo;}
  const CodasipTargetLowering* getTargetLowering() const override {return &TLInfo;}
  const TargetRegisterInfo* getRegisterInfo() const override {return &InstrInfo.getRegisterInfo();}
  const InstrItineraryData* getInstrItineraryData() const override {return &InstrItins;}
  const CodasipSelectionDAGInfo* getSelectionDAGInfo() const override {return &TSInfo;}
private:
  /// this is here because llvm 2.8 wants some subtarget features
  bool codasip;
};

}

#endif
